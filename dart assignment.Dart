// Dart program demonstrating data types

void main() {
  // Integer data type
  int age = 30;
  print('Age: $age');

  // Double data type
  double weight = 65.5;
  print('Weight: $weight');

  // String data type
  String name = 'John Doe';
  print('Name: $name');

  // List data type
  List<String> fruits = ['Apple', 'Banana', 'Orange'];
  print('Fruits: $fruits');

  // Map data type
  Map<String, dynamic> person = {
    'name': 'Alice',
    'age': 25,
    'city': 'New York'
  };
  print('Person: $person');
}
